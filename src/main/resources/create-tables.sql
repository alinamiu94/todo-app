create Table APP_USER(
	ID INT PRIMARY KEY,
	NAME VARCHAR(50) NOT NULL,
	PASSWORD VARCHAR(100) NOT NULL,
	CREATED_AT TIMESTAMP
);

create Table TASK(
	ID INT PRIMARY KEY,
	NAME VARCHAR(50) NOT NULL,
	DESCRIPTION VARCHAR(700) NOT NULL,
	USER_ID INT,
	DUE_DATE TIMESTAMP,
	STATUS VARCHAR(30),
	CREATED_AT TIMESTAMP,
	FINISHED_AT TIMESTAMP,
	FOREIGN KEY(USER_ID) REFERENCES APP_USER(ID)
);